let n = prompt("Тоогоо оруулна уу");

let z, x, c, v;

if (n % 2 == 0) {
  n %= 100;
  if (n % 4 == 0) {
    alert("Тэгш тоо, 4-т хуваагдана");
  } else {
    alert("Тэгш тоо, 4-т хуваагдахгүй");
  }
} else {
  z = parseInt(n / 1000);
  n %= 1000;
  x = parseInt(n / 100);
  n %= 100;
  c = parseInt(n / 10);
  v = n % 10;
  if ((z + x + c + v) % 3 == 0) {
    alert("Сондгой тоо, 3-т хуваагдана");
  } else {
    alert("Сондгой тоо, 3-т хуваагдахгүй");
  }
}
